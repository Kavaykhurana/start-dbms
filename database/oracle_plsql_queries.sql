-- 7. 5 COMPLEX JOINS
-- =========================================================

-- Join 1: highest ROI investors in Fintech.
SELECT
    inv.investor_name,
    s.startup_name,
    sec.sector_name,
    i.funding_round,
    fm.net_profit_inr,
    ra.risk_score,
    ROUND(((fm.net_profit_inr * 12 * 5 * (i.equity_percentage / 100)) / i.invested_amount_inr) * 100, 2) AS roi_proxy_pct
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN investments i ON i.startup_id = s.startup_id
JOIN investors inv ON inv.investor_id = i.investor_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
LEFT JOIN risk_analysis ra ON ra.investment_id = i.investment_id
WHERE sec.sector_name = 'Fintech'
  AND fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
ORDER BY roi_proxy_pct DESC;

-- Join 2: partnership leverage.
SELECT s.startup_name, sec.sector_name, p.partner_name, p.deal_type, p.impact_score, md.simulated_trend_value
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN partnerships p ON p.startup_id = s.startup_id
JOIN market_data md ON md.startup_id = s.startup_id
WHERE md.market_date = (SELECT MAX(market_date) FROM market_data WHERE startup_id = s.startup_id)
ORDER BY p.impact_score DESC;

-- Join 3: investor portfolio diversification.
SELECT inv.investor_name, sec.sector_name, COUNT(*) AS active_bets, SUM(i.invested_amount_inr) AS deployed_capital
FROM investors inv
JOIN investments i ON i.investor_id = inv.investor_id
JOIN startups s ON s.startup_id = i.startup_id
JOIN sectors sec ON sec.sector_id = s.sector_id
WHERE i.status = 'Active'
GROUP BY inv.investor_name, sec.sector_name
ORDER BY deployed_capital DESC;

-- Join 4: runway stress with investor exposure.
SELECT s.startup_name, inv.investor_name, fm.runway_months, fm.burn_rate_inr, fm.churn_rate, ra.risk_score
FROM startups s
JOIN investments i ON i.startup_id = s.startup_id
JOIN investors inv ON inv.investor_id = i.investor_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
LEFT JOIN risk_analysis ra ON ra.investment_id = i.investment_id
WHERE fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
  AND fm.runway_months < 12
ORDER BY fm.runway_months ASC, ra.risk_score DESC;

-- Join 5: funding efficiency across six entities.
SELECT
    s.startup_name,
    sec.sector_name,
    SUM(i.invested_amount_inr) AS total_funding,
    ROUND(fm.ltv_inr / NULLIF(fm.cac_inr, 0), 2) AS ltv_cac_ratio,
    AVG(p.impact_score) AS avg_partner_impact,
    md.simulated_trend_value
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN investments i ON i.startup_id = s.startup_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
LEFT JOIN partnerships p ON p.startup_id = s.startup_id
JOIN market_data md ON md.startup_id = s.startup_id
WHERE fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
  AND md.market_date = (SELECT MAX(market_date) FROM market_data WHERE startup_id = s.startup_id)
GROUP BY s.startup_name, sec.sector_name, fm.ltv_inr, fm.cac_inr, md.simulated_trend_value
ORDER BY ltv_cac_ratio DESC;

