-- Subquery 1: startups whose burn rate is higher than their sector average.
SELECT s.startup_name, sec.sector_name, fm.burn_rate_inr
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
WHERE fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
  AND fm.burn_rate_inr > (
      SELECT AVG(fm2.burn_rate_inr)
      FROM startups s2
      JOIN financial_metrics fm2 ON fm2.startup_id = s2.startup_id
      WHERE s2.sector_id = s.sector_id
        AND fm2.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s2.startup_id)
  );

-- Subquery 2: startups above sector LTV/CAC average.
SELECT s.startup_name, sec.sector_name, ROUND(fm.ltv_inr / NULLIF(fm.cac_inr, 0), 2) AS ltv_cac_ratio
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
WHERE fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
  AND (fm.ltv_inr / NULLIF(fm.cac_inr, 0)) > (
      SELECT AVG(fm2.ltv_inr / NULLIF(fm2.cac_inr, 0))
      FROM startups s2
      JOIN financial_metrics fm2 ON fm2.startup_id = s2.startup_id
      WHERE s2.sector_id = s.sector_id
        AND fm2.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s2.startup_id)
  );

-- Subquery 3: investors deploying above their own investor-type average.
SELECT inv.investor_name, inv.investor_type
FROM investors inv
WHERE (
    SELECT NVL(SUM(i.invested_amount_inr), 0)
    FROM investments i
    WHERE i.investor_id = inv.investor_id
) > (
    SELECT AVG(type_total.total_deployed)
    FROM (
        SELECT inv2.investor_type, inv2.investor_id, NVL(SUM(i2.invested_amount_inr), 0) AS total_deployed
        FROM investors inv2
        LEFT JOIN investments i2 ON i2.investor_id = inv2.investor_id
        GROUP BY inv2.investor_type, inv2.investor_id
    ) type_total
    WHERE type_total.investor_type = inv.investor_type
);

-- Subquery 4: startups with better runway than sector average.
SELECT s.startup_name, sec.sector_name, fm.runway_months
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
WHERE fm.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s.startup_id)
  AND fm.runway_months > (
      SELECT AVG(fm2.runway_months)
      FROM startups s2
      JOIN financial_metrics fm2 ON fm2.startup_id = s2.startup_id
      WHERE s2.sector_id = s.sector_id
        AND fm2.metric_month = (SELECT MAX(metric_month) FROM financial_metrics WHERE startup_id = s2.startup_id)
  );

-- Subquery 5: startups with market trend above sector average.
SELECT s.startup_name, sec.sector_name, md.simulated_trend_value
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN market_data md ON md.startup_id = s.startup_id
WHERE md.market_date = (SELECT MAX(market_date) FROM market_data WHERE startup_id = s.startup_id)
  AND md.simulated_trend_value > (
      SELECT AVG(md2.simulated_trend_value)
      FROM startups s2
      JOIN market_data md2 ON md2.startup_id = s2.startup_id
      WHERE s2.sector_id = s.sector_id
        AND md2.market_date = (SELECT MAX(market_date) FROM market_data WHERE startup_id = s2.startup_id)
  );

