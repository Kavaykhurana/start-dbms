-- Startup Investment Analysis & Decision System
-- Oracle / PL/SQL viva script
-- Tested syntax target: Oracle 19c or later.

-- =========================================================
-- 1. CORE TABLES
-- =========================================================

CREATE TABLE sectors (
    sector_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sector_name VARCHAR2(60) NOT NULL UNIQUE,
    sector_description VARCHAR2(255) NOT NULL,
    base_risk_score NUMBER(3) NOT NULL,
    CONSTRAINT chk_sectors_risk CHECK (base_risk_score BETWEEN 0 AND 100)
);

CREATE TABLE investors (
    investor_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    investor_name VARCHAR2(120) NOT NULL UNIQUE,
    investor_type VARCHAR2(30) NOT NULL,
    email VARCHAR2(120) NOT NULL UNIQUE,
    hq_city VARCHAR2(80) NOT NULL,
    dry_powder_inr NUMBER(15,2) NOT NULL,
    risk_preference VARCHAR2(20) NOT NULL,
    created_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT chk_investor_type CHECK (investor_type IN ('Angel', 'VC', 'Corporate VC', 'Family Office')),
    CONSTRAINT chk_investor_risk_pref CHECK (risk_preference IN ('Low', 'Medium', 'High')),
    CONSTRAINT chk_investor_dry_powder CHECK (dry_powder_inr >= 0)
);

CREATE TABLE startups (
    startup_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sector_id NUMBER NOT NULL,
    startup_name VARCHAR2(120) NOT NULL UNIQUE,
    city VARCHAR2(80) NOT NULL,
    state VARCHAR2(80) NOT NULL,
    founding_year NUMBER(4) NOT NULL,
    funding_stage VARCHAR2(20) NOT NULL,
    annual_revenue_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    current_valuation_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    employee_count NUMBER DEFAULT 0 NOT NULL,
    equity_committed_pct NUMBER(5,2) DEFAULT 0 NOT NULL,
    website VARCHAR2(160) NOT NULL,
    CONSTRAINT fk_startups_sector FOREIGN KEY (sector_id) REFERENCES sectors (sector_id),
    CONSTRAINT chk_startup_stage CHECK (funding_stage IN ('Pre-Seed', 'Seed', 'Series A', 'Series B', 'Series C')),
    CONSTRAINT chk_startup_year CHECK (founding_year BETWEEN 2005 AND 2035),
    CONSTRAINT chk_startup_amounts CHECK (annual_revenue_inr >= 0 AND current_valuation_inr >= 0),
    CONSTRAINT chk_startup_equity CHECK (equity_committed_pct BETWEEN 0 AND 100)
);

CREATE TABLE investments (
    investment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    investor_id NUMBER NOT NULL,
    startup_id NUMBER NOT NULL,
    funding_round VARCHAR2(20) NOT NULL,
    security_type VARCHAR2(30) NOT NULL,
    investment_date DATE NOT NULL,
    invested_amount_inr NUMBER(15,2) NOT NULL,
    equity_percentage NUMBER(5,2) NOT NULL,
    post_money_valuation_inr NUMBER(15,2) NOT NULL,
    status VARCHAR2(20) DEFAULT 'Active' NOT NULL,
    CONSTRAINT fk_investments_investor FOREIGN KEY (investor_id) REFERENCES investors (investor_id),
    CONSTRAINT fk_investments_startup FOREIGN KEY (startup_id) REFERENCES startups (startup_id),
    CONSTRAINT chk_investment_round CHECK (funding_round IN ('Pre-Seed', 'Seed', 'Series A', 'Series B', 'Series C', 'Bridge')),
    CONSTRAINT chk_investment_security CHECK (security_type IN ('Equity', 'SAFE', 'Convertible Note')),
    CONSTRAINT chk_investment_status CHECK (status IN ('Active', 'Exited', 'Written Off')),
    CONSTRAINT chk_investment_amount CHECK (invested_amount_inr > 0),
    CONSTRAINT chk_investment_equity CHECK (equity_percentage > 0 AND equity_percentage <= 100)
);

CREATE TABLE financial_metrics (
    metric_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    startup_id NUMBER NOT NULL,
    metric_month DATE NOT NULL,
    revenue_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    net_profit_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    cash_reserve_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    customer_count NUMBER DEFAULT 0 NOT NULL,
    mrr_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    arr_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    cac_inr NUMBER(12,2) DEFAULT 0 NOT NULL,
    ltv_inr NUMBER(12,2) DEFAULT 0 NOT NULL,
    burn_rate_inr NUMBER(15,2) DEFAULT 0 NOT NULL,
    runway_months NUMBER(5,2) DEFAULT 0 NOT NULL,
    churn_rate NUMBER(5,2) DEFAULT 0 NOT NULL,
    CONSTRAINT fk_financial_metrics_startup FOREIGN KEY (startup_id) REFERENCES startups (startup_id),
    CONSTRAINT uq_financial_period UNIQUE (startup_id, metric_month),
    CONSTRAINT chk_financial_nonnegative CHECK (
        revenue_inr >= 0
        AND cash_reserve_inr >= 0
        AND customer_count >= 0
        AND mrr_inr >= 0
        AND arr_inr >= 0
        AND cac_inr >= 0
        AND ltv_inr >= 0
        AND burn_rate_inr >= 0
        AND runway_months >= 0
        AND churn_rate BETWEEN 0 AND 100
    )
);

CREATE TABLE market_data (
    market_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    startup_id NUMBER NOT NULL,
    simulated_trend_value NUMBER(5,2) NOT NULL,
    competitor_score NUMBER(5,2) NOT NULL,
    market_date DATE NOT NULL,
    CONSTRAINT fk_market_data_startup FOREIGN KEY (startup_id) REFERENCES startups (startup_id),
    CONSTRAINT uq_market_period UNIQUE (startup_id, market_date),
    CONSTRAINT chk_market_scores CHECK (simulated_trend_value BETWEEN 0 AND 100 AND competitor_score BETWEEN 0 AND 100)
);

CREATE TABLE partnerships (
    partner_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    startup_id NUMBER NOT NULL,
    partner_name VARCHAR2(140) NOT NULL,
    deal_type VARCHAR2(10) NOT NULL,
    impact_score NUMBER(5,2) NOT NULL,
    announced_date DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_partnerships_startup FOREIGN KEY (startup_id) REFERENCES startups (startup_id),
    CONSTRAINT chk_partnership_type CHECK (deal_type IN ('LOI', 'Pilot')),
    CONSTRAINT chk_partnership_impact CHECK (impact_score BETWEEN 0 AND 10)
);

CREATE TABLE risk_analysis (
    risk_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    investment_id NUMBER NOT NULL,
    risk_score NUMBER(5,2) NOT NULL,
    factors VARCHAR2(500) NOT NULL,
    assessment_date DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_risk_investment FOREIGN KEY (investment_id) REFERENCES investments (investment_id),
    CONSTRAINT uq_risk_snapshot UNIQUE (investment_id, assessment_date),
    CONSTRAINT chk_risk_score CHECK (risk_score BETWEEN 0 AND 100)
);

