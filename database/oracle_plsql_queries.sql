CREATE OR REPLACE PROCEDURE sp_CalculateROI (
    p_investment_id IN NUMBER,
    p_roi_percentage OUT NUMBER
) AS
BEGIN
    SELECT ROUND(
        ((fm.net_profit_inr * 12 * 5 * (i.equity_percentage / 100))
        / NULLIF(i.invested_amount_inr, 0)) * 100,
        2
    )
    INTO p_roi_percentage
    FROM investments i
    JOIN financial_metrics fm ON fm.startup_id = i.startup_id
    WHERE i.investment_id = p_investment_id
      AND fm.metric_month = (
          SELECT MAX(metric_month)
          FROM financial_metrics
          WHERE startup_id = i.startup_id
      );
END;
/

CREATE OR REPLACE PROCEDURE sp_GetRecommendation (
    p_result OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
        SELECT
            ROW_NUMBER() OVER (ORDER BY recommendation_score DESC, startup_name) AS recommendation_rank,
            startup_id,
            startup_name,
            sector_name,
            funding_stage,
            ROUND(ltv_cac_ratio, 2) AS ltv_cac_ratio,
            ROUND(risk_score, 2) AS risk_score,
            ROUND(recommendation_score / 10, 2) AS decision_score,
            CASE
                WHEN recommendation_score >= 75 THEN 'INVEST'
                WHEN recommendation_score >= 60 THEN 'WATCH'
                ELSE 'AVOID'
            END AS signal
        FROM (
            SELECT
                s.startup_id,
                s.startup_name,
                sec.sector_name,
                s.funding_stage,
                fm.ltv_inr / NULLIF(fm.cac_inr, 0) AS ltv_cac_ratio,
                NVL(AVG(ra.risk_score), 50) AS risk_score,
                (
                    0.50 * LEAST(100, (fm.ltv_inr / NULLIF(fm.cac_inr, 0)) * 16.67)
                    + 0.20 * GREATEST(0, 100 - ((fm.cac_inr / max_cac.max_cac_inr) * 100))
                    + 0.30 * (100 - NVL(AVG(ra.risk_score), 50))
                ) AS recommendation_score
            FROM startups s
            JOIN sectors sec ON sec.sector_id = s.sector_id
            JOIN financial_metrics fm ON fm.startup_id = s.startup_id
            LEFT JOIN investments i ON i.startup_id = s.startup_id
            LEFT JOIN risk_analysis ra ON ra.investment_id = i.investment_id
            CROSS JOIN (
                SELECT MAX(cac_inr) AS max_cac_inr
                FROM financial_metrics
                WHERE metric_month = (SELECT MAX(metric_month) FROM financial_metrics)
            ) max_cac
            WHERE fm.metric_month = (
                SELECT MAX(metric_month)
                FROM financial_metrics
                WHERE startup_id = s.startup_id
            )
            AND fm.cac_inr > 0
            GROUP BY
                s.startup_id,
                s.startup_name,
                sec.sector_name,
                s.funding_stage,
                fm.ltv_inr,
                fm.cac_inr,
                max_cac.max_cac_inr
        )
        ORDER BY recommendation_score DESC, startup_name
        FETCH FIRST 5 ROWS ONLY;
END;
/

