CREATE OR REPLACE VIEW vw_TopStartups AS
SELECT
    s.startup_id,
    s.startup_name,
    sec.sector_name,
    s.funding_stage,
    ROUND(fm.ltv_inr / NULLIF(fm.cac_inr, 0), 2) AS ltv_cac_ratio,
    fm.runway_months,
    fm.churn_rate
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
WHERE fm.metric_month = (
    SELECT MAX(metric_month)
    FROM financial_metrics
    WHERE startup_id = s.startup_id
)
AND fm.cac_inr > 0
AND (fm.ltv_inr / NULLIF(fm.cac_inr, 0)) >= 4
AND fm.runway_months >= 9;

CREATE OR REPLACE VIEW vw_HighRiskAlerts AS
SELECT
    s.startup_id,
    s.startup_name,
    sec.sector_name,
    fm.burn_rate_inr,
    fm.runway_months,
    fm.churn_rate,
    ROUND(AVG(ra.risk_score), 2) AS avg_risk_score
FROM startups s
JOIN sectors sec ON sec.sector_id = s.sector_id
JOIN financial_metrics fm ON fm.startup_id = s.startup_id
LEFT JOIN investments i ON i.startup_id = s.startup_id
LEFT JOIN risk_analysis ra ON ra.investment_id = i.investment_id
WHERE fm.metric_month = (
    SELECT MAX(metric_month)
    FROM financial_metrics
    WHERE startup_id = s.startup_id
)
AND fm.runway_months < 6
GROUP BY s.startup_id, s.startup_name, sec.sector_name, fm.burn_rate_inr, fm.runway_months, fm.churn_rate;

