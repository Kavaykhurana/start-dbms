CREATE OR REPLACE TRIGGER trig_EquityCheck
FOR INSERT OR UPDATE OF equity_percentage, startup_id, status
ON investments
COMPOUND TRIGGER
    TYPE t_startup_ids IS TABLE OF investments.startup_id%TYPE INDEX BY PLS_INTEGER;
    g_startup_ids t_startup_ids;
    g_count PLS_INTEGER := 0;

    BEFORE EACH ROW IS
    BEGIN
        g_count := g_count + 1;
        g_startup_ids(g_count) := :NEW.startup_id;
    END BEFORE EACH ROW;

    AFTER STATEMENT IS
        v_total_equity NUMBER(7,2);
    BEGIN
        FOR idx IN 1 .. g_count LOOP
            SELECT NVL(SUM(equity_percentage), 0)
            INTO v_total_equity
            FROM investments
            WHERE startup_id = g_startup_ids(idx)
              AND status = 'Active';

            IF v_total_equity > 100 THEN
                RAISE_APPLICATION_ERROR(-20001, 'Investment rejected: total startup equity cannot exceed 100 percent.');
            END IF;
        END LOOP;
    END AFTER STATEMENT;
END trig_EquityCheck;
/

CREATE OR REPLACE TRIGGER trig_RiskUpdate
AFTER UPDATE OF burn_rate_inr, churn_rate, runway_months
ON financial_metrics
FOR EACH ROW
BEGIN
    UPDATE risk_analysis ra
    SET ra.risk_score = LEAST(
        100,
        ROUND(
            CASE
                WHEN :NEW.burn_rate_inr >= 20000000 THEN 35
                WHEN :NEW.burn_rate_inr >= 12000000 THEN 25
                ELSE 15
            END
            + CASE
                WHEN :NEW.runway_months < 6 THEN 35
                WHEN :NEW.runway_months < 12 THEN 20
                ELSE 10
            END
            + LEAST(30, :NEW.churn_rate * 4),
            2
        )
    )
    WHERE ra.investment_id IN (
        SELECT investment_id
        FROM investments
        WHERE startup_id = :NEW.startup_id
          AND status = 'Active'
    )
    AND ra.assessment_date = (
        SELECT MAX(ra2.assessment_date)
        FROM risk_analysis ra2
        WHERE ra2.investment_id = ra.investment_id
    );
END;
/

